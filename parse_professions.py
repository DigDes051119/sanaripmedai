def build_professions_database():
    print("[RAG] Создание базы данных профессий...")
    os.makedirs("data/professions_kb", exist_ok=True)
    
    docs = []
    for name, info in PROFESSIONS_DATA.items():
        doc_id = f"prof_{name.lower().replace(' ', '_').replace('/', '_').replace('(', '').replace(')', '')}"
        
        # Формируем читаемый структурированный текст для RAG
        content_lines = [
            f"Медицинская специальность: {name}",
            f"Категория: {info['category']}",
            f"Описание: {info['description']}",
            f"Когда обращаться к этому специалисту: {info['when_to_visit']}",
            f"Основные жалобы и симптомы: {', '.join(info['symptoms'])}",
            f"Заболевания, которые лечит: {', '.join(info['diseases'])}",
            f"Методы диагностики и лечения: {', '.join(info['methods'])}"
        ]
        content_text = "\n".join(content_lines)
        
        doc = {
            "id": doc_id,
            "title": f"Профессия: {name}",
            "content": content_text,
            "url": f"https://sanarip.med.ai/professions/{urllib.parse.quote(name)}"
        }
        
        # Сохраняем индивидуальный файл
        with open(f"data/professions_kb/{doc_id}.json", "w", encoding="utf-8") as f:
            json.dump(doc, f, ensure_ascii=False, indent=2)
            
        docs.append(doc)
        
    print(f"[RAG] Всего сохранено профессий: {len(docs)}")
    
    # Построение нового индекса
    print("[RAG] Построение индекса для профессий...")
    index = SimpleTFIDFIndex()
    for doc in docs:
        index.add_document(doc["id"], doc["title"], doc["content"], doc["url"])
        
    index.build_index()
    index.save("data/professions_index.json")
    print("[RAG] Индекс профессий успешно создан!")

if __name__ == "__main__":
    build_professions_database()
